int ISkanji(int code)
{
	int jcode = Option.jcode == JCODE_AUTO ? File.jcode : Option.jcode;

	if (code >= 0x100)
		return 0;

	switch (jcode) {
	case JCODE_ASCII:
		return kanji_map_ascii[code & 0xff];
	case JCODE_SJIS:
		return kanji_map_sjis[code & 0xff];
	case JCODE_EUC:
		return kanji_map_euc[code & 0xff];
	case JCODE_UTF8:
		return kanji_map_utf8[code & 0xff];
	default:
		return 0;
	}
}

